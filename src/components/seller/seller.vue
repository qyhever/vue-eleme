				</div>
				<ul class="supports" v-if="seller.supports">
					<li class="support-item border-1px" v-for="(item, index) in seller.supports" :key="index">
						<span class="icon" :class="classMap[item.type]"></span>
						<span class="text">{{item.description}}</span>
					</li>
				</ul>
			</div>
			<!-- 分割组件 -->
			<split></split>
			<div class="photo">
				<h1 class="title">商家实景</h1>
				<div class="photo-wrap" ref="photo-wrap">
					<ul class="photo-list" ref="photo-list">
						<li class="photo-item" v-for="(pic,index) in seller.pics" :key="index">
							<img :src="pic" alt="" width="120" height="90">
						</li>
					</ul>
				</div>
			</div>
			<!-- 分割组件 -->
			<split></split>
			<div class="info">
				<h1 class="title border-1px"></h1>
				<ul>
					<li class="info-item" v-for="(info, index) in seller.infos">
						{{info}}
					</li>
				</ul>
			</div>
		</div>
	</div>
</template>

<script>
import BScroll from 'better-scroll';
import VStar from 'components/star/star';
import split from 'components/split/split';
export default {
	components: {VStar, split},
	props: {
		seller: {type: Object}
	},
	data() {
		return {
			classMap: ['decrease', 'discount', 'special', 'invoice', 'guarantee'],
			favorite: false
		};
	},
	computed: {
		favoriteText() {
			return this.favorite ? '已收藏' : '收藏';
		}
	},
	watch: {
		'seller'() {
			this.$nextTick(() => {
				this._initScroll();
				this._initPhotoScroll();
			});
		}
	},
	created() {
		
	},
	mounted() {
		this.$nextTick(() => {
			this._initScroll();
			this._initPhotoScroll();
		});
	},
	methods: {
		_initScroll() {
			if (!this.scroll) {
				this.scroll = new BScroll(this.$refs.seller, {
					click: true
				});
			} else {
				this.scroll.refresh();
			}
		},
		_initPhotoScroll() {
			if (this.seller.pics) {
				let photoWidth = 120;
				let margin = 6;
				let width = (photoWidth + margin) * this.seller.pics.length - margin;
				this.$refs['photo-list'].style.width = width + 'px';
				this.$nextTick(() => {
					if (!this.photoScroll) {
						this.photoScroll = new BScroll(this.$refs['photo-wrap'], {
							click: true,
							scrollX: true,
							eventPassthrough: 'vertical'
						});
					} else {
						this.photoScroll.refresh();
					}
					
				});
			}
		},
		toggleFavorite(event) {
			if (!event._constructed) {
				return;
			}
			this.favorite = !this.favorite;
		}
	}
}
</script>

